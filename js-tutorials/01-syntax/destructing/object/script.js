/******************************************************************************************************************
OLD SYNTAX
******************************************************************************************************************/
const person1 = {
    firstName: 'Mark',
    lastName: 'Side',
    age: 28, 
};

let firstName1 = person1.firstName;
let lastName1 = person1.lastName;
let age1 = person1.age;

console.log(firstName1); // output: Mark
console.log(lastName1);  // output: Side
console.log(age1);       // output: 28
/******************************************************************************************************************
OLD SYNTAX
******************************************************************************************************************/

/******************************************************************************************************************
NEW SYNTAX
******************************************************************************************************************/
const person2 = {
    firstName: 'Mark',
    lastName: 'Side',
    age: 28, 
};

let { firstName, lastName, age } = person2;

console.log(firstName); // output: Mark
console.log(lastName);  // output: Side
console.log(age);       // output: 28
/**************************************************
EQUIVALENT TO
**************************************************/
let { firstName: fname, lastName: lname, age } = person2;

console.log(fname); // output: Mark
console.log(lname); // output: Side
console.log(age);   // output: 28
/******************************************************************************************************************
NEW SYNTAX
******************************************************************************************************************/