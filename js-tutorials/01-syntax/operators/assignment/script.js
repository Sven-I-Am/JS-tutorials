// AN ASSIGNMENT OPERATOR ASSIGNS A VALUE TO ITS LEFT OPERAND BASED ON THE VALUE OF ITS RIGHT OPERAND

/******************************************************************************************************************
ASSIGNMENT OPERATOR: "="
******************************************************************************************************************/
let i;
console.log(i); // output: undefined
i = 1;
console.log(i); // output: 1
/******************************************************************************************************************
ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
MULTIPLICATION ASSIGNMENT OPERATOR: "*="
******************************************************************************************************************/
let j = 2;
console.log(j); // output: 2;

j *= 5; // j = j * 5;
console.log(j); // output: 10;
/******************************************************************************************************************
MULTIPLICATION ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
EXPONENTIATION ASSIGNMENT OPERATOR: : "**="
******************************************************************************************************************/
let k = 2;
console.log(k); // output: 2;

k **= 3; // k = k ** 3;
console.log(k); // output: 8;
/******************************************************************************************************************
EXPONENTIATION ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
DIVISION ASSIGNMENT OPERATOR: "/="
******************************************************************************************************************/
let l = 8;
console.log(l); // output: 8;

l /= 2; // l = l / 2;
console.log(l); // output: 4;
/******************************************************************************************************************
DIVISION ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
REMAINDER ASSIGNMENT OPERATOR: "%="
******************************************************************************************************************/
let m = 6;
console.log(m); // output: 6;

m %= 2; // m = m % 2;
console.log(m); // output: 0;
/******************************************************************************************************************
REMAINDER ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
ADDITION ASSIGNMENT OPERATOR: "+="
******************************************************************************************************************/
let n = 1;
console.log(n); // output: 1;

n += 1; // n = n + 1;
console.log(n); // output: 2;
/******************************************************************************************************************
ADDITION ASSIGNMENT OPERATOR
******************************************************************************************************************/

/******************************************************************************************************************
SUBTRACTION ASSIGNMENT OPERATOR: "-="
******************************************************************************************************************/
let o = 1;
console.log(o); // output: 1;

o -= 1; // o = o - 1;
console.log(o); // output: 0;
/******************************************************************************************************************
SUBTRACTION ASSIGNMENT OPERATOR
******************************************************************************************************************/